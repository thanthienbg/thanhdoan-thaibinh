from pydantic_settings import BaseSettings

class Settings(BaseSettings):
    PROJECT_NAME: str = "QA Chatbot"
    VERSION: str = "1.0.0"
    API_V1_STR: str = "/api/v1"
    
    ANTHROPIC_API_KEY: str
    OPENAI_API_KEY: str
    
    VECTOR_DB_HOST: str = "localhost"
    VECTOR_DB_PORT: int = 8000
    
    class Config:
        env_file = ".env"

settings = Settings()